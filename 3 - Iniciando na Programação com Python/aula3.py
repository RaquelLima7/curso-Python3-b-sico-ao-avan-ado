# Aspas simples
print('Raquel Lima')
print(1, 'Raquel Lima"')

# Aspas duplas
print("Raquel Lima")
print(2, "Raquel 'Lima'") # 2 Raquel 'Lima'

# Escape
print("Raquel \"Lima\"") # Raquel "Lima"

# r
print(r"Raquel \"Lima\"") # Raquel \"Lima\"
